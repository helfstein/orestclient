using System;
using System.Collections.Generic;
using System.Linq;
using Newtonsoft.Json;
using ORest.Models;

namespace ORestClient.Samples.ODataModels {
    //=============================================================================================
    public class Order {

        #region Constructor
        //-----------------------------------------------------------------------------------------
        public Order() {
            Items = new List<Item>();
            Statuses = new List<Status>();
        }
        //-----------------------------------------------------------------------------------------
        #endregion

        #region Properties
        //-----------------------------------------------------------------------------------------
        public string ObjectId { get; set; }
        //-----------------------------------------------------------------------------------------
        public DateTime DataCriacao { get; set; }
        //-----------------------------------------------------------------------------------------
        public DateTime? DataEntrega { get; set; }
        //-----------------------------------------------------------------------------------------
        public string Payer { get; set; }
        //-----------------------------------------------------------------------------------------
        public string Partner { get; set; }
        //-----------------------------------------------------------------------------------------
        public string Status { get; set; }
        //-----------------------------------------------------------------------------------------
        public string StatusColor { get; set; }
        //-----------------------------------------------------------------------------------------
        public string StatusColor2 { get; set; }
        //-----------------------------------------------------------------------------------------
        public string Inco { get; set; }
        //-----------------------------------------------------------------------------------------
        public string IncoText { get; set; }
        //-----------------------------------------------------------------------------------------
        public string Description { get; set; }
        //-----------------------------------------------------------------------------------------
        public string OrderQtyF { get; set; }
        //-----------------------------------------------------------------------------------------
        public string QtyUnit { get; set; }
        //-----------------------------------------------------------------------------------------
        public string Planta { get; set; }
        //-----------------------------------------------------------------------------------------
        public string PlantaName { get; set; }
        //-----------------------------------------------------------------------------------------
        public string Entrega { get; set; }
        //-----------------------------------------------------------------------------------------
        public string Nf { get; set; }
        //-----------------------------------------------------------------------------------------
        public string Netwrt { get; set; }
        //-----------------------------------------------------------------------------------------
        [JsonProperty("ToItem")]
        public EntityContent<List<Item>> EntityItems {
            get => new EntityContent<List<Item>> {
                Results = Items
            };
            set {
                if (value != null && value.Results.Any()) {
                    Items = value.Results;
                }
            }
        }
        //-----------------------------------------------------------------------------------------
        public List<Item> Items { get; set; }
        //-----------------------------------------------------------------------------------------
        public List<Status> Statuses { get; set; }
        //-----------------------------------------------------------------------------------------
        [JsonProperty("ToTimeline")]
        public EntityContent<List<Status>> EntityStatus {
            get => new EntityContent<List<Status>> {
                    Results = Statuses
                };
            set {
                if (value != null && value.Results.Any()) {
                    Statuses = value.Results;
                }
            }
        }
        //-----------------------------------------------------------------------------------------
        public string St_Desc { get; set; }
        //-----------------------------------------------------------------------------------------
        public string St_Font { get; set; }
        //-----------------------------------------------------------------------------------------
        public string St_Color { get; set; }
        //-----------------------------------------------------------------------------------------
        public string St_Color2 { get; set; }
        //-----------------------------------------------------------------------------------------
        #endregion

    }
    //=============================================================================================
}